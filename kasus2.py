def hitung(angka1, operator, angka2):
    if operator == '+':
        hasil = angka1 + angka2
    elif operator == '-':
        hasil = angka1 - angka2
    elif operator == '*':
        hasil = angka1 * angka2
    else: 
        operator == '/'
        hasil = angka1 / angka2 
        return hasil